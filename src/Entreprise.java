public class Entreprise {
    private String nom, rue, adEnt;

    public Entreprise(){}

    public String getNom(){return this.nom;}
    public String getRue(){return this.rue;}
    public String getNumAdrese(){return this.adEnt;}

    public void setNom(String _nom){this.nom = _nom;}
    public void setRue(String _rue){this.rue = _rue;}
    public void setNumAdrese(String _numAdresse){this.adEnt = _numAdresse;}
}